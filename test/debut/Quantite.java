package debut;

public class Quantite {
	    private double value;
	    private String unit;

	    public Quantite(double value, String unit) {
	        this.value = value;
	        this.unit = unit;
	    }

	    public double getValue() {
	        return value;
	    }

	    public String getUnit() {
	        return unit;
	    }
	}



